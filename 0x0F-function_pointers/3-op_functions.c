#include "3-calc.h"

/**
 * op_add - a function
 * @a: input
 * @b: input
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - a function
 * @a: input
 * @b: input
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - a function
 * @a: input
 * @b: inut
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - a function
 * @a: input
 * @b: input
 * Return: the division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - a function
 * @a: input
 * @b: input
 * Return: remainder
 */
int op_mod(int a, int b)
{
return (a % b);
}
