#include <stdlib.h>
/**
 * add - a function
 * @a: input
 * @b: input
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - a function
 * @a: input
 * @b: input
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mod - a function
 * @a: input
 * @b: input
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * mul - a function
 * @a: input
 * @b: input
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
